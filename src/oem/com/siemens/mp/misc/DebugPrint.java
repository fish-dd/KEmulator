package com.siemens.mp.misc;

public class DebugPrint {

	public static void print(String s) {
		System.out.println(s);
	}

	public static void print(String s, int n) {
		System.out.println(s);
	}

	public static void print(String s, int n1, int n2) {
		System.out.println(s);
	}

	public static void print(String s, int n1, int n2, int n3) {
		System.out.println(s);
	}

}
